    public boolean ValidateDelivery(){
        boolean check = false;
        int val = DeliveryCombo.getSelectedIndex();
        if(val == 0){
            JOptionPane.showMessageDialog(null, "Please select Delivery Status.");
            DeliveryCombo.requestFocusInWindow();
        } else{
            check = true;
        }
        return check;
    }


    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        java.sql.Date sqlDate = new java.sql.Date(new java.util.Date().getTime());
        if(ValidateOrderNo() && ValidateSupplierName() && ValidateItem() && ValidateQty() && ValidateUnitPrice()
                && ValidateTotal() && ValidateDelivery() ){
        String sql = "insert into suppliers (orderno,supplier_name,item,quantity,unitprice,total,Status,date) values (?,?,?,?,?,?,?,?)";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, OrderNoTxt.getText());
            pst.setString(2, SupplierNameTxt.getText());
            pst.setString(3, ItemTxt.getText());
            pst.setString(4, QtyTxt.getText());
            pst.setString(5, UnitPriceTxt.getText());
            pst.setString(6, TotalTxt.getText());
            String value = DeliveryCombo.getSelectedItem().toString();
            pst.setString(7, value);
            pst.setDate(8,sqlDate);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Supplier Info added.");
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Please Check for the following errors: \nQuantity, Unit Price and Total must be numbers.\nThere are duplicate order" +
                    " numbers.");
        }
        UpdateTable();
        } 
}//GEN-LAST:event_AddBtnActionPerformed

