    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        if(ValidateCenterId() && ValidateCenterUsername() && ValidateCenterPassword() && ValidateCenterName() &&
                ValidateCenterCity()){
        String sql = "Insert into centerlogindetails (CenterId, username, password, CenterName, CenterCity) values(?,?,?,?,?)";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, CenterIdTxt.getText());
            pst.setString(2, CenterUsernameTxt.getText());
            pst.setString(3, CenterPasswordTxt.getText());
            pst.setString(4, CenterNameTxt.getText());
            pst.setString(5, CenterCityTxt.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Center Info added.");
            // Clear the textfields after saving.
            CenterIdTxt.setText(null);
            CenterUsernameTxt.setText(null);
            CenterPasswordTxt.setText(null);
            CenterNameTxt.setText(null);
            CenterCityTxt.setText(null);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Center ID already exists.\nCenter Username already exists.\nCenter ID has to be a number.");
        }
        UpdateTable();
      }
}//GEN-LAST:event_AddBtnActionPerformed

