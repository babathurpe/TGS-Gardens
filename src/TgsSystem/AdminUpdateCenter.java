    private void SearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBtnActionPerformed
        // TODO add your handling code here:
        try{
            //Select all information from table where center name matches
            String sql = "select * from centerlogindetails where CenterId=?";
            pst = conn.prepareStatement(sql);
            //Get text from Seacrh textbox
            pst.setString(1, SearchTxt.getText());
            //Search table for center name
            rs = pst.executeQuery();
            //if center name is available
            if(rs.next()){
                //Fetch and put available data from table into appropriate fields
                 String setId = rs.getString("CenterId");
                 CenterIdTxt.setText(setId);
                 String setUsername = rs.getString("username");
                 CenterUsernameTxt.setText(setUsername);
                 String setPassword = rs.getString("password");
                 CenterPasswordTxt.setText(setPassword);
                 String setCenterName = rs.getString("CenterName");
                 CenterNameTxt.setText(setCenterName);
                 String setCenterCity = rs.getString("CenterCity");
                 CenterCityTxt.setText(setCenterCity);
            }else{
                UpdateTable();
                JOptionPane.showMessageDialog(null, "Sorry, No match found. Search again.");
                CenterIdTxt.setText(null);
                CenterUsernameTxt.setText(null);
                CenterPasswordTxt.setText(null);
                CenterNameTxt.setText(null);
                CenterCityTxt.setText(null);
            }
        } catch(Exception e){

        }
}//GEN-LAST:event_SearchBtnActionPerformed

